package activity01;

public class Sum03 {
    //This program implements for loop to sum whole number 1 - 100
    public static void main(String args []){
        int total = 0;
        for(int count = 1; count <= 100; count++){
            total = total + count;
        }
        System.out.print(total);
    }
}
